# formats for Time and DateTime objects
Time::DATE_FORMATS[:calendar_date] = "%B %-d, %Y"
Time::DATE_FORMATS[:short_calendar_date] = "%b %e %Y"
Time::DATE_FORMATS[:shorter_calendar_date] = "%b %e"
Time::DATE_FORMATS[:day_month_year] = "%d-%m-%y"
Time::DATE_FORMATS[:full_date_time_format] = "%b %d %Y, %I:%M %P"
